public class HeroBag {
    private int yakuso = 0;
    private int shodoku = 0;
    private int sake = 0;
    private int enmaku = 0;
    private int tabako = 0;

    public int getYakuso(){
        return this.yakuso;
    }
    public setYakuso(int yakuso){
        this.yakuso = yakuso;
    }
    public int getShodoku(){
        return this.shodoku;
    }
    public setShodoku(int shodoku){
        this.shodoku = shodoku;
    }
    public int getSake(){
        return this.sake;
    }
    public setSake(int sake){
        this.sake = sake;
    }
    public int getEnmake(){
        return this.enmaku;
    }
    public setEnmaku(int enmaku){
        this.enmaku = enmaku;
    }
    public int getTabako(){
        return this.tabako;
    }
    public setTabako(int tabako){
        this.tabako = tabako;
    }

    
}